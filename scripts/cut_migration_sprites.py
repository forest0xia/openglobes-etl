"""Cut migration species composite images into individual sprites.

Reads 2x2 grid images generated by ChatGPT, splits into quadrants,
crops each to its opaque content, and saves with proper species names.

Usage:
    python scripts/cut_migration_sprites.py /path/to/migration_species/
"""
